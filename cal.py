# Basic Calu for Addition + - * and divission 

num_1 = float("Enter ist number :")
num_2 = float("Enter 2nd number :")

def add(num_1 , num_2):
    return(num_1+num_2)

def sub(num_1 , num_2):
    return(num_1-num_2)

def mul(num_1 , num_2):
    return(num_1*num_2)

def div(num_1 , num_2):
    return(num_1/num_2)
